interface BrowseEntityIconProps {
  iconName?: string
  className?: string
  fallbackClassName?: string
}

export function BrowseEntityIcon({
  iconName,
  className,
  fallbackClassName,
}: BrowseEntityIconProps) {
  const Icon = iconName ? ICONS[iconName] : null

  if (!Icon) {
    return (
      <Compass
        className={cn("h-4.5 w-4.5 text-editorial-muted", fallbackClassName)}
      />
    )
  }

  return <Icon className={cn("h-4.5 w-4.5", className)} />
}
